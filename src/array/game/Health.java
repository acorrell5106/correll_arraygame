package array.game;

public class Health {
    public int hpx;
    public int hpy;
    
    public Health (int a, int b) {
        this.hpx = a;
        this.hpy = b;
    }

    public int getHpx() {
        return hpx;
    }

    public int getHpy() {
        return hpy;
    }

    public void setHpx(int hpx) {
        this.hpx = hpx;
    }

    public void setHpy(int hpy) {
        this.hpy = hpy;
    }
    
}

